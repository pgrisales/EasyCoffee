    public void numeroArboles(){
        int[] numero=new int[6];
        for (int i = 0; i <this.arboles.size(); i++) {
            switch(this.arboles.get(i).getVariedad()){
                case "Típica":{
                    numero[0]++;
                    break;
                }
                case "Borbón":{
                    numero[1]++;
                    break;
                }
                case "Maragogipe":{
                    numero[2]++;
                    break;
                }
                case "Tabi":{
                    numero[3]++;
                    break;
                }
                case "Caturra":{
                    numero[4]++;
                    break;
                }
                case "Variedad Colombia":{
                    numero[5]++;
                    break;
                }
            }
        }
        for (int i = 0; i < 6; i++) {
            this.jTable1.setValueAt(numero[i], i, 1);
        }
    }
